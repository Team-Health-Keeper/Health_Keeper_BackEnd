// 나이로 연령대 판단
const getAgeGroup = (age) => {
  if (age < 7) return "유아기";
  if (age < 13) return "유소년";
  if (age < 20) return "청소년";
  if (age >= 65) return "어르신";
  return "성인";
};

// 연령대별 측정 항목 가져오기
const getMeasurementItems = (ageGroup) => {
  return ageGroups[ageGroup] || ageGroups["성인"];
};

